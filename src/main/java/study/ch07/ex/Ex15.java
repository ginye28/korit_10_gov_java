package study.ch07.ex;

public class Ex15 {
    public static void main(String[] args) {
        int season = 3;
        if (season == 1) {
            System.out.println("봄");
        } else if (season == 2) {
            System.out.println("여름");
        } else if (season == 3) {
            System.out.println("가을");
        } else if (season == 4) {
            System.out.println("겨울");
        } else System.out.println("잘못된 입력");
    }
}
